_current_lang = "de"


def get_language() -> str:
    return _current_lang


def set_language(lang: str):
    """Setzt die aktive Sprache (z. B. aus dem Settings-Fenster)."""
    global _current_lang
    _current_lang = lang


def load_app_language(settings_path: str):
    """Lädt die gespeicherte Sprache aus der Settings-Datei. Fällt bei
    Fehlern auf Deutsch zurück."""
    global _current_lang
    try:
        if os.path.exists(settings_path):
            with open(settings_path, "r", encoding="utf-8") as f:
                data = json.load(f)
                _current_lang = data.get("language", "de")
    except Exception:
        _current_lang = "de"


def t(key):
    lang = _current_lang
    lang_dict = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    val = lang_dict.get(key)
    if val is None:
        val = TRANSLATIONS["en"].get(key, key)
    return val
